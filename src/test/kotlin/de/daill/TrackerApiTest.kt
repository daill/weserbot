package de.daill

import de.daill.protocol.TrackerApi
import de.daill.protocol.VanguardStats
import org.junit.jupiter.api.Assertions
import org.junit.jupiter.api.Test

class TrackerApiTest {

    @Test
    fun testVanguardLevel() {
        var tracker = TrackerApi()
        var stats = tracker.getVanguardStats("daill") as VanguardStats
        var overview = stats.data!!.segments[0]
        var level = overview.stats.getValue("level") as HashMap<String, Object>
        Assertions.assertNotNull(level.getValue("displayValue"))
    }
}